// this loop executes once a second to open all detail views
var loop = function() {
	if (!$) return;
	var tr = trList[idx++];	

	if (!tr) {
		clearTimeout(tmo);

		// update all values
		updateDates();

		// close the detail views
		trList.find('td:nth-child(8) a').each(function() {
			this.click();
		});
		return;
	}

	tr = $(tr);
	var a = tr.find('td:nth-child(8) a');
	// click on the order number
	if (!a[0]) {
		clearTimeout(tmo);
		alert("Pls filter on 'executed'");
		return;
	}
	a[0].click();
};

var updateDates = function() {

	// update the headers
	$(hList[2]).text('Commissions');
	$(hList[3]).text('Purchase lineDates');
	$(hList[4]).text('Purchase Prices');
	$(hList[8]).text('Gross Proceeds');
	$(hList[9]).text('Profits');

	var totalProfits = 0;

	// for each row
	for (var i=0;i<idx-1;i++) {
		var lineComission = 0;
		var stockPrice = 0;
		var cellStockCount = 4;
		var cellStockPrice = -1;
		var RSU = true;

		// get the selling proce per share for the table
		var sellingPricePerShare = parseFloat($(trList[i]).find('td:nth-child(7)').text().replace('$', ''));

		var lineGrossRevenue = 0;
		var lineProfit = 0;

		// if selling ESPP
		if ($(trList[i]).find('td:nth-child(2)').text() == 'ESPP') {
			cellStockCount = 3;
			cellStockPrice = 2;

			RSU = false;
		}

		// calculate the line-level lineComission
		for (var j=3;j<7;j++) {
			try  {
				lineComission += parseFloat($(tr2List[i]).find('div .alignRight table table tr:nth-child('+j+') b').html().replace('$', ''));
			} catch(e) {};
		}

		var lineStockPriceList = '';
		var lineSharesCount = '';
		var lineDates = '';

		// for all grants in this transaction (max. 100)
		for (var j=3;j<100;j++) {

			if (lineSharesCount) lineSharesCount += '<br />';

			var sharesCount = $(tr2List[i]).find('div#wrap>div>table>tbody>tr:nth-child(2)>td:nth-child(1) table tr:nth-child('+j+') td:nth-child('+cellStockCount+')').text();

			sharesCount = parseFloat(sharesCount.replace(',', ''));

			// break if no more lines
			if (!sharesCount) break;

			lineSharesCount += sharesCount; // string

			lineGrossRevenue += sellingPricePerShare * sharesCount;

			if (lineDates) lineDates += '<br />';

			var date = $(tr2List[i]).find('div#wrap>div>table>tbody>tr:nth-child(2)>td:nth-child(1) table tr:nth-child('+j+') td:nth-child(1)').text().replace(/\s/, '');
			lineDates += date;

			var dateObj = new Date(date);

			if (RSU || (!RSU && (dateObj <= new Date('12/31/2011') || dateObj >= new Date('12/31/2013')))) {
				// if RSU or ESPP, but before 2012 or after 2013
				// use the market value
				for (var tries=0;tries<10;tries++) {
					stockPrice = adobeStockValues[date];
					if (stockPrice) break;

					dObj = new Date(date);
					dObj.setDate(dObj.getDate() - 1);

					var day = dObj.getDate();
					if (day < 10) day = "0" + day;

					var month = dObj.getMonth()+1;
					if (month < 10) month = "0" + month;

					date = month  + '/' + day + '/' + dObj.getFullYear();
				}
			} else {
				// if ESPP between and taxes were not paid on the diff
				// use the grant purchase value
				var stockPrice = $(tr2List[i]).find('div#wrap>div>table>tbody>tr:nth-child(2)>td:nth-child(1) table tr:nth-child('+j+') td:nth-child('+cellStockPrice+')');
				stockPrice = stockPrice.text();
				stockPrice = stockPrice.replace(/\s\$/, '');
				stockPrice = parseFloat(stockPrice);
			}

			lineProfit += stockPrice * sharesCount;

			if (lineStockPriceList) lineStockPriceList += '<br />';
			lineStockPriceList += '$' + stockPrice;
		}

		// DOM updates

		$(trList[i]).find('td:nth-child(6)').html(lineSharesCount);

		if (lineDates != 0) {
			$(trList[i]).find('td:nth-child(4)').html(lineDates);
		}

		if (lineComission) {
			lineComission = parseInt(lineComission*100)/100;
			$(trList[i]).find('td:nth-child(3)').html("$" + lineComission);
		}

		if (lineGrossRevenue) {
			lineGrossRevenue = parseInt(lineGrossRevenue*100)/100;
			$(trList[i]).find('td:nth-child(9)').html("$" + lineGrossRevenue);

			if (lineProfit) {
				lineProfit = lineGrossRevenue - lineProfit - lineComission;
				lineProfit = parseInt(lineProfit*100)/100;
				$(trList[i]).find('td:nth-child(10)').html("$" + lineProfit);

				totalProfits += lineProfit;
			}

		}

		if (lineStockPriceList != 0) {
			$(trList[i]).find('td:nth-child(5)').html(lineStockPriceList);
		}

	}

	totalProfits = parseInt(totalProfits*100)/100;

	// total profit
	$('<p style="text-align: right; color: black; font-size: 1.3em;">Total Profits: $'+totalProfits+'</p>').insertAfter('#MainTBL');
}

var initRomaniaTaxes = function() {
	// the list of table rows that we want to change
	window.trList = $('#MainTBL table tr:nth-child(2n+3)');

	// the list of table rows where the detailed view shows up when clicking on the order #
	window.tr2List = $('#MainTBL table tr:nth-child(2n+4)');

	// header list
	window.hList = $($('#MainTBL table tr')[0]).find('td');

	if (window.tmo) clearTimeout(window.tmo);

	// the line we're working on
	window.idx = 0;

	window.tmo = setInterval(loop, 1000);
}

